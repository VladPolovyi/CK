// Helper functions to get achievements by type
export const getGladiatorAchievements = () => 
  PVP_ACHIEVEMENTS.filter(a => a.type === 'gladiator')

export const getLegendAchievements = () => 
  PVP_ACHIEVEMENTS.filter(a => a.type === 'legend')

export const getRank1Achievements = () => 
  PVP_ACHIEVEMENTS.filter(a => a.type === 'rank1')

export const getRank1SoloAchievements = () => 
  PVP_ACHIEVEMENTS.filter(a => a.type === 'rank1solo')

export const getStrategistAchievements = () => 
  PVP_ACHIEVEMENTS.filter(a => a.type === 'strategist')

export const getRank1BlitzAchievements = () => 
  PVP_ACHIEVEMENTS.filter(a => a.type === 'rank1blitz')

export const getActiveAchievements = () => 
  PVP_ACHIEVEMENTS.filter(a => a.isActive)

export const getAchievementsByExpansion = (expansion) => 
  PVP_ACHIEVEMENTS.filter(a => a.expansion === expansion)

export const getAchievementsBySeason = (season) => 
  PVP_ACHIEVEMENTS.filter(a => a.season === season)

